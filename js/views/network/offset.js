class Offset {
    /**
     * @param {number} top 
     * @param {number} left 
     * @param {number} bottom 
     * @param {number} right 
     */
    constructor(
        top,
        left,
        bottom,
        right
    ) {
        this.top = top;
        this.left = left;
        this.bottom = bottom;
        this.right = right;
    }
}

export default Offset;
